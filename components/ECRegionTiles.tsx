// TILES

interface RegionTileProps {
    regionName: ECRegionNames;
    children: ReactNode;
}

function RegionTile({ regionName, children }: RegionTileProps) {
    let title = '';

    switch (regionName) {
        case ECRegionNames.CostaCentro:
            title = 'Costa Centro';
            break;
        case ECRegionNames.CostaNorte:
            title = 'Costa Norte';
            break;
        case ECRegionNames.Guayaquil:
            title = 'Guayaquil';
            break;
        case ECRegionNames.Quito:
            title = 'Sierra Quito';
            break;
        case ECRegionNames.SierraOriente:
            title = 'Sierra Oriente';
            break;
        case ECRegionNames.SierraSur:
            title = 'Sierra Sur';
            break;
    }

    return (
        <div className={classNames('region-tile', `${regionName}`)}>
            <div className="region-image">{children}</div>
            <div className="region-title">{regionName}</div>
        </div>
    );
}

